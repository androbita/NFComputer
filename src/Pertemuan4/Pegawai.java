package Pertemuan4;

/**
 *
 * @author pranadana
 */
public class Pegawai {
    public String id;
    public String nama;
    public double gaji;

    public Pegawai() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public double getGaji() {
        return gaji;
    }

    public void setGaji(double gaji) {
        this.gaji = gaji;
    }
    
    
}
